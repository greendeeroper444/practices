// typeof operator - returns the type of a variable
let testVar = 'Hello';
console.log(typeof testVar); // 'string'
let testNum = 42;
console.log(typeof testNum); // 'number'

// instanceof operator - checks if object is instance of specific class
let testArray = [1, 2, 3];
console.log(testArray instanceof Array); // true
let testDate = new Date();
console.log(testDate instanceof Date); // true